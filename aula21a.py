def soma(a = 0, b = 0, c = 0):
    """
    -> Função soma 3 valores
    :param a: primeiro valor
    :param b: segundo valor
    :param c: terceiro valor
    :return: sem retorno
    """
    s = a + b + c
    return s

r1 = soma(1,4,)
r2 = soma(2,4,7)
r3 = soma(0,8)
print(f'Os resultados foram {r1} ,{r2} e {r3}')